inline float _sphere(glm::vec3 x,float r){
    return glm::dot(x,x) - r*r;
}
float sphere(glm::vec3 x){
    return _sphere(x,1.0f);
}
glm::vec3 df_sphere(glm::vec3 x){
    return 2.0f*x;
}
inline implicit* getSphereModel(const char* mtlPath,float interval){
    return new implicit(sphere, df_sphere,mtlPath,0.0f,interval,glm::vec2(-1.3f,1.3f),glm::vec2(-1.3f,1.3f),glm::vec2(-1.3f,1.3f));
}

float torus(glm::vec3 p){
    float t1 = glm::dot(p,p)+3.0;
    glm::vec2 _p(p[0],p[1]);
    float t2 = 16.0f*(glm::dot(_p,_p));
    return t1*t1-t2;
}
glm::vec3 df_torus(glm::vec3 p){
    auto x = p[0];
    auto y = p[1];
    auto z = p[2];

    float t1 = glm::dot(p,p)+3.0;

    return glm::vec3(
        (4.0f*t1-32.0f)*x,
        (4.0f*t1-32.0f)*y,
        4.0f*t1*z
    );
}
inline implicit* getTorusModel(const char* mtlPath, float interval){
    auto rng = glm::vec2(-4.2f,4.2f);
    return new implicit(torus,df_torus,mtlPath,0.0f,interval,rng,rng,rng);
}

float genus2(glm::vec3 p){
    auto x = p[0];
    auto y = p[1];
    auto z = p[2];

    float t1 = (x*x+y*y);

    return 2.0f*y*(y*y-3.0f*x*x)*(1-z*z)+t1*t1-(9.0f*z*z-1)*(1-z*z);
}
inline implicit* getGenus2Model(const char* mtlPath, float interval){
    auto rng = glm::vec2(-2.2f,2.2f);
    return new implicit(genus2,nullptr,mtlPath,0.0f,interval,rng,rng,rng);
}

float SphereMerging(glm::vec3 p){
    float sphereDist1, sphereDist2;
    auto sphereDistVec1 = p-glm::vec3(0.0,0.0,1.5);
    sphereDist1 = glm::dot(sphereDistVec1,sphereDistVec1);
    auto sphereDistVec2 = p-glm::vec3(0.0,0.0,-1.5);
    sphereDist2 = glm::dot(sphereDistVec2,sphereDistVec2);
    float sum = 1.0f/(sphereDist1+1e-6) + 1.0f/(sphereDist2 + 1e-6);
    return 1.0f/sum;
}
